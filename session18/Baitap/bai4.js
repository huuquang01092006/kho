let num = Number(prompt("Nhập một số bất kỳ: "));
switch (num) {
    case 0:
        console.log("Số không");
        break;
    case 1:
        console.log("Số một");
        break;
    case 2:
        console.log("Số hai");
        break;
    case 3:
        console.log("Số ba");
        break;
    case 4:
        console.log("Số bốn");
        break;
    case 5:
        console.log("Số năm");
        break;
    case 6:
        console.log("Số sáu");
        break;
    case 7:
        console.log("Số bảy");
        break;
    case 8:
        console.log("Số tám");
        break;
    case 9:        
        console.log("Số chín");
        break;
    default:
        console.log("Không phải số từ 1 đến 9");
        break;
}